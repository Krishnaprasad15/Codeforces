import timeit as time
import random as rand
import matplotlib.pyplot as mtplt


# Insertion Sort
def insertion_sort(array):
    n= len(array);
    for ind in range(1, n):
        ele = array[ind]
        index = ind - 1
        while index >= 0:
            if array[index] > ele:
                array[index + 1] = array[index]
                index = index - 1
            else:
                break
        array[index + 1] = ele

