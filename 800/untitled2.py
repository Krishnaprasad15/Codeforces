# Bubble Sort
def bubble_sort(array):
    n= len(array)
    for ele1 in range(n):
        for ele in range(n-ele1-1):
            if(array[ele]>array[ele+1]):
                array[ele],array[ele+1]=array[ele+1],array[ele]

# Function to generate a random array of a given size
def generate_random(size):
    return [rand.randint(1, 1000) for _ in range(size)]

# Function to benchmark sorting algorithms
def benchmark(sort_func, array):
    n=len(array)
    setup_code = f"from __main__ import {sort_func}, generate_random; arr = generate_random({n})"
    stmt = f"{sort_func}(arr)"

    # Measure the execution time
    exe_time = time.timeit(stmt, setup=setup_code, number=10)

    return exe_time

# Benchmark parameters
sizes = [5, 10, 20, 50, 100, 200, 500, 1000,2000]  # Adjust as needed

# Results dictionary to store benchmark results
results = {'Insertion Sort': [], 'Selection Sort': [], 'Bubble Sort': []}

# Run benchmarks for each sorting algorithm and array size
for size in sizes:
    array = generate_random(size)

    # Insertion Sort
    ins_time = benchmark('insertion_sort', array)
    results['Insertion Sort'].append(ins_time)
    print(f"Input Size: {size}, Insertion Sort Execution Time: {ins_time:.6f} seconds")


    # Selection Sort
    sel_time = benchmark('sel_sort', array)
    results['Selection Sort'].append(sel_time)
    print(f"Input Size: {size}, Selection Sort Execution Time: {ins_time:.6f} seconds")

    # Bubble Sort
    bub_time = benchmark('bubble_sort', array)
    results['Bubble Sort'].append(bub_time)
    print(f"Input Size: {size}, Bubble Sort Execution Time: {ins_time:.6f} seconds")

# Plotting the results
mtplt.plot(sizes, results['Insertion Sort'], label='Insertion Sort', color='orange')
mtplt.plot(sizes, results['Selection Sort'], label='Selection Sort', color='blue')
mtplt.plot(sizes, results['Bubble Sort'], label='Bubble Sort', color='green')

mtplt.xlabel('Array Size')
mtplt.ylabel('Execution Time (sec)')
mtplt.title('Benchmark Sorting Algorithm')
mtplt.legend()
mtplt.show()